package genericTest;

public class ObjectToolDemo {
	public static void main(String[] args) {
		ObjectTool<String> s1 = new ObjectTool<>();
		s1.setObject("张三");
		System.out.println(s1);
		
		ObjectTool<Integer> i1 = new ObjectTool<>();
		i1.setObject(new Integer(30));
		System.out.println(i1);
		
		ObjectTool<String> i3 = new ObjectTool<>();
		i3.show("哈哈");
		i3.show(new Integer(30));
		i3.show(true);
	}
}
